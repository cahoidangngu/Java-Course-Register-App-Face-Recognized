    private void searchStudentListCourseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchStudentListCourseBtnActionPerformed
        // TODO add your handling code here:
        CourseIdFieldRegister.setText("");
        String courseId = courseIdFieldFind.getText();
        boolean isReistered = registerCourseCheckBox.isSelected();        
        
        if(isReistered)
        Controller.userViewRegisterCourseController(student.getUserId()+"", student.getUserRole(), courseId,this, this);
        else
        Controller.userViewCourseByIdController(courseId, false, this);
    }//GEN-LAST:event_searchStudentListCourseBtnActionPerformed

    private void registerStudentToCouresBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerStudentToCouresBtnActionPerformed
        // TODO add your handling code here:
        courseIdFieldFind.setText("");
        registerCourseCheckBox.setSelected(false);
        String courseId = CourseIdFieldRegister.getText();
        Controller.userRegisterByCourseIdController(courseId, this);
        CourseIdFieldRegister.setText("");
        registerCourseCheckBox.setSelected(true);
        renderTable(Course_Model.getCoursesByUserId(Controller.getUser().getUserId(), "student", ""));
    }//GEN-LAST:event_registerStudentToCouresBtnActionPerformed

    private void withdrawStudentFromCouresBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_withdrawStudentFromCouresBtnActionPerformed
        // TODO add your handling code here:
        courseIdFieldFind.setText("");
        registerCourseCheckBox.setSelected(false);
        String courseId = CourseIdFieldRegister.getText();
        Controller.userWithdrawByCourseIdController(courseId, this);
        CourseIdFieldRegister.setText("");
        registerCourseCheckBox.setSelected(true);
        renderTable(Course_Model.getCoursesByUserId(Controller.getUser().getUserId(), "student", ""));
    }//GEN-LAST:event_withdrawStudentFromCouresBtnActionPerformed

