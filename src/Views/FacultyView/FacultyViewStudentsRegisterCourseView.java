    private void renderComboBox(){
        courseIdClassComboBox.removeAllItems();
        courseIdClassComboBox.addItem("<none>");
        List<Course> listCourseId = Course_Model.getCoursesByUserId(faculty.getUserId(), "faculty", "");
        for(Course it : listCourseId){
            courseIdClassComboBox.addItem(it.getCourseId());
        }
    }
    
    
     private void readUsers(){
        int cnt = 1;
        for(User u : studentList){
            dtmUserList.addRow(u.toObject(cnt++));
        }
    }
    
    @Override
    public <E> void renderTable(List<E> listStudentResult){
        dtmUserList = (DefaultTableModel) studentsCourseTable.getModel();
        studentList = (List<User>) listStudentResult;
        dtmUserList.setRowCount(0);
        readUsers();
    }

